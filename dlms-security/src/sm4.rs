fn u8_to_u32(b: &[u8]) -> u32 {
    u32::from_be_bytes([b[0], b[1], b[2], b[3]])
}

fn u32_to_u8(v: u32) -> [u8; 4] {
    v.to_be_bytes()
}

fn tau(a: u32) -> u32 {
    let b = a.to_be_bytes();
    let c = [
        SBOX[b[0] as usize],
        SBOX[b[1] as usize],
        SBOX[b[2] as usize],
        SBOX[b[3] as usize],
    ];
    u32::from_be_bytes(c)
}

fn l_transform(b: u32) -> u32 {
    let c = tau(b);
    c ^ c.rotate_left(2) ^ c.rotate_left(10) ^ c.rotate_left(18) ^ c.rotate_left(24)
}

fn l_prime_transform(b: u32) -> u32 {
    let c = tau(b);
    c ^ c.rotate_left(13) ^ c.rotate_left(23)
}

fn key_expansion(key: &Sm4Key) -> [u32; 32] {
    let mk = [
        u8_to_u32(&key.0[0..4]) ^ FK[0],
        u8_to_u32(&key.0[4..8]) ^ FK[1],
        u8_to_u32(&key.0[8..12]) ^ FK[2],
        u8_to_u32(&key.0[12..16]) ^ FK[3],
    ];

    let mut rk = [0u32; 32];
    let mut k = mk;
    for i in 0..32 {
        rk[i] = k[0] ^ l_prime_transform(k[1] ^ k[2] ^ k[3] ^ CK[i]);
        k = [k[1], k[2], k[3], rk[i]];
    }
    rk
}

fn encrypt_block(rk: &[u32; 32], input: &Sm4Block) -> Sm4Block {
    let mut x = [
        u8_to_u32(&input.0[0..4]),
        u8_to_u32(&input.0[4..8]),
        u8_to_u32(&input.0[8..12]),
        u8_to_u32(&input.0[12..16]),
    ];

    for i in 0..32 {
        let tmp = x[1] ^ x[2] ^ x[3] ^ rk[i];
        let new = x[0] ^ l_transform(tmp);
        x = [x[1], x[2], x[3], new];
    }

    let mut output = [0u8; 16];
    output[0..4].copy_from_slice(&u32_to_u8(x[3]));
    output[4..8].copy_from_slice(&u32_to_u8(x[2]));
    output[8..12].copy_from_slice(&u32_to_u8(x[1]));
    output[12..16].copy_from_slice(&u32_to_u8(x[0]));
    Sm4Block(output)
}

/// Encrypt a single 16-byte block with SM4
pub fn sm4_encrypt(key: &Sm4Key, input: &Sm4Block) -> Sm4Block {
    let rk = key_expansion(key);
    encrypt_block(&rk, input)
}

/// Decrypt a single 16-byte block with SM4
pub fn sm4_decrypt(key: &Sm4Key, input: &Sm4Block) -> Sm4Block {
    let rk = key_expansion(key);
    let rk_rev: [u32; 32] = std::array::from_fn(|i| rk[31 - i]);
    encrypt_block(&rk_rev, input)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_sbox_basic() {
        assert_eq!(SBOX[0x00], 0xD6);
        assert_eq!(SBOX[0x01], 0x90);
        assert_eq!(SBOX[0xFF], 0x48);
    }

    #[test]
    fn test_tau() {
        // tau should apply SBOX to each byte
        let result = tau(0x00000000);
        let expected = (SBOX[0] as u32) << 24
            | (SBOX[0] as u32) << 16
            | (SBOX[0] as u32) << 8
            | SBOX[0] as u32;
        assert_eq!(result, expected);
    }

    #[test]
    fn test_key_expansion_deterministic() {
        let key = Sm4Key::from([0x01; 16]);
        let rk1 = key_expansion(&key);
        let rk2 = key_expansion(&key);
        assert_eq!(rk1, rk2);
    }
}
