//! SM4 block cipher - Pure Rust implementation (GB/T 32907-2016)

/// SM4 key (16 bytes)
#[derive(Clone, Copy, PartialEq, Eq)]
pub struct Sm4Key(pub [u8; 16]);

/// SM4 block (16 bytes)
#[derive(Clone, Copy, PartialEq, Eq)]
pub struct Sm4Block(pub [u8; 16]);

impl From<[u8; 16]> for Sm4Key {
    fn from(v: [u8; 16]) -> Self {
        Self(v)
    }
}

impl From<[u8; 16]> for Sm4Block {
    fn from(v: [u8; 16]) -> Self {
        Self(v)
    }
}

impl core::fmt::Debug for Sm4Key {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        write!(f, "Sm4Key({:02X?})", self.0)
    }
}

impl core::fmt::Debug for Sm4Block {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        write!(f, "Sm4Block({:02X?})", self.0)
    }
}

